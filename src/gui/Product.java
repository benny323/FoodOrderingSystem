    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        //get testfeild string
        String productID = txtProductID.getText();
        String produtName = txtProductName.getText();
        String price = txtPrice.getText();
        String description = txtDescription.getText();

        File f = new File("product.txt");
        boolean duplicate = false;

        try {
            Scanner s = new Scanner(f);
            while(s.hasNextLine()){
                String row = s.nextLine();
                String data[] = row.split("#");
                if(data[0].equals(productID)){
                    duplicate = true;
                    break;
                }
            }
        } catch (FileNotFoundException ex) {

        }
        if(duplicate){
            JOptionPane.showMessageDialog(this, "Item existed");
        }else{
            try{
                PrintWriter p = new PrintWriter(new FileWriter("product.txt",true));
                p.println(productID + "#" + produtName + "#" + price + "#" + description + "#" /*+ fragile + "#" + Nfragile*/   );
                p.close();
                
            } catch (IOException ex) {
            }
            JOptionPane.showMessageDialog(this, "Item Registration Successful.");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

